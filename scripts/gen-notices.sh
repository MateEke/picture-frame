#!/usr/bin/env bash
# Assemble THIRD_PARTY_NOTICES.txt (stdout) from three sources:
#   Go: go-licenses report (deps linked into the binary)
#   Frontend: Vite's build.license output (client JS bundle)
#   Fonts: @fontsource/* LICENSE files, appended explicitly: they ship CSS+woff2 but no
#          JS, so no JS-graph license tool detects them.
# `make notices` redirects stdout to internal/licenses/THIRD_PARTY_NOTICES.txt.
set -euo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
VITE_MD="$ROOT/web/.svelte-kit/output/client/.vite/license.md"
GOLICENSES=github.com/google/go-licenses@v1.6.0 # pinned; `go run` keeps it out of go.mod/go.sum
SELF_MODULE=github.com/MateEke/picture-frame

if [ ! -f "$VITE_MD" ]; then
	echo "ERROR: $VITE_MD missing, run 'make build-ui' first (Vite build.license emits it)." >&2
	exit 1
fi

cat <<'EOF'
THIRD-PARTY NOTICES: picture-frame

This file is GENERATED by `make notices`. Do not edit by hand.
picture-frame is distributed under Apache-2.0 (see LICENSE). It bundles the
third-party components listed below, each under its own license reproduced here.

================================================================================
Go modules (linked into the binary)
================================================================================
EOF
go run "$GOLICENSES" report ./cmd/picture-frame --template /dev/stdin 2>/dev/null <<TMPL
{{range .}}{{if ne .Name "$SELF_MODULE"}}
--------------------------------------------------------------------------------
## {{.Name}} ({{.LicenseName}})
--------------------------------------------------------------------------------

{{.LicenseText}}
{{end}}{{end}}
TMPL

cat <<'EOF'

================================================================================
Frontend: bundled npm packages (client bundle)
================================================================================

EOF
cat "$VITE_MD"

printf '\n'
cat <<'EOF'
================================================================================
Bundled fonts (CSS-imported; added explicitly)
================================================================================
EOF
for fontlic in "$ROOT"/web/node_modules/@fontsource*/*/LICENSE; do
	[ -f "$fontlic" ] || continue
	pkg="${fontlic#*/node_modules/}" # e.g. @fontsource/google-sans/LICENSE
	printf '\n## %s\n\n' "${pkg%/LICENSE}"
	cat "$fontlic"
	printf '\n'
done
